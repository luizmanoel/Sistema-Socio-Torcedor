    public void configTabela(){//Preenchimento inicial da tabela
        DefaultTableModel tableModel = (DefaultTableModel) this.jTable1.getModel();
        tableModel = new DefaultTableModel (new String[]{"Email"},0);
        
        for(int i = 0; i < this.telAlt.size(); i++){
            String dadoLinha [] = {this.telAlt.get(i).getNumero()}; 
            
            tableModel.addRow(dadoLinha);
        
        
        }
        this.jTable1.setModel(tableModel);
    
    
    
    
    }

