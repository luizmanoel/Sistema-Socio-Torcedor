    public void removeCategoria(){//Testar antes para ver se pode mesmo remover o ST
        
        int x = this.jListCategoria.getSelectedIndex();
        
        CategoriaDAO db = new CategoriaDAO();
        Categoria cat = new Categoria();
        ArrayList<Object> listaCat = db.consulta(cat);
        Categoria aRemover = (Categoria) listaCat.get(x);
        
        //Teste pra ver se alguem eh desta categoria
        
        SocioTorcedorDAO stdao = new SocioTorcedorDAO();
        SocioTorcedor st = new SocioTorcedor();
        
        ArrayList<Object> listaST = stdao.consulta(st);
        
        for(int i = 0; i < listaST.size(); i++){
            st = (SocioTorcedor) listaST.get(i);
            if(st.checaCategoria(aRemover)){
                JOptionPane.showMessageDialog(null, "Incapaz de Remover, altere antes as categorias dos sÃ³cios participantes.");
                
                this.dispose();
                return;//Pausa execucao pra nao ficar abrindo direto
            }
        
        
        }
        
        JOptionPane.showMessageDialog(null,"Vai dar pra remover sim.");//continua
        
        db.exclui(aRemover);
        
        
    
    }
